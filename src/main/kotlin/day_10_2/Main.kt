object Pipes {
    var pipes = lines.lines()
    val width: Int get() = pipes[0].length
    val height: Int get() = pipes.size
    fun pipeAt(x: Int, y: Int): Char = pipes[y][x]
    fun pipeAt(pos: Point): Char = pipes[pos.y][pos.x]

    fun isFirstRow(pos: Point) = pos.y == 0
    fun isLastRow(pos: Point) = pos.y == height - 1
    fun isRightRow(pos: Point) = pos.x == width - 1
    fun isLeftRow(pos: Point) = pos.x == 0
//    val uD = Point(0, -1)  //up direction
    val uD = Pair(0, -1)  //up direction
    val rD = Pair(1, 0)  //right direction
    val dD = Pair(0, 1)  //down direction
    val lD = Pair(-1, 0)  //left direction

    var currentPos: Point
    var direction: Pair<Int, Int>
    var firstDirectionn: Pair<Int, Int>
    private var NO_DIRECION = Pair(0,0)
    private var isNorthSouthStartPipe : Boolean
    init {
        currentPos = findStartPos()
        isNorthSouthStartPipe = getFirstDirection().second != 0 // fuggoleges pipe van az 'S' alatt?
        println("starting from: $currentPos")
        direction = NO_DIRECION
        firstDirectionn = getFirstDirection()
    }

    fun findStartPos(): Point {
        pipes.forEachIndexed { y, e ->
            var x = e.indexOf('S')
            if (x != -1) {
                return Point(x, y)
            }
        }
        error("No start position found")
    }

    fun getFirstDirection() : Pair<Int, Int>{
        if(canGoUp()) return uD
        if(canGoDown()) return dD
        if(canGoRight()) return rD
        return lD
    }

    fun canGoUp (pos: Point) : Boolean {
        if (isFirstRow(pos)) return false
        val uc = pipeAt(pos.x, pos.y - 1) // upper char
        if (uc == '|' || uc == '7' || uc == 'F') return true else return false
    }

    fun canGoUp() = canGoUp(currentPos)
    fun canGoDown(pos: Point): Boolean {
        if (isLastRow(pos)) return false
        val dc = pipeAt(pos.x, pos.y + 1) // bottom char
        if (dc == '|' || dc == 'L' || dc == 'J') return true else return false
    }

    fun canGoDown() = canGoDown(currentPos)
    fun canGoRight(pos: Point): Boolean {
        if (isRightRow(pos)) return false
        val rc = pipeAt(pos.x + 1, pos.y) // upper char
        if (rc == '-' || rc == '7' || rc == 'J') return true else return false
    }

    fun canGoRight() = canGoRight(currentPos)
    fun getNextDirection() = when (pipeAt(currentPos)) {
        '|' -> Pair(0, direction.second)
        '-' -> Pair(direction.first, 0)
        'L' -> Pair(direction.first + 1, direction.second - 1)
        'J' -> Pair(direction.first - 1, direction.second - 1)
        '7' -> Pair(direction.first - 1, direction.second + 1)
        'F' -> Pair(direction.first + 1, direction.second + 1)
        'S' -> NO_DIRECION
        else -> error("Invalid pipe found")
    }

    fun step(): Pair<Char, Point> {
        if (direction == NO_DIRECION) {
            direction = getFirstDirection()
            println("firstdirection: $direction")
        }
        currentPos = Point(currentPos.x + direction.first, currentPos.y + direction.second)
        direction = getNextDirection()

        var pipe : Char = pipeAt(currentPos)
        return Pair(pipe, currentPos )
    }

    fun getSPipe(): Char {
        if (firstDirectionn.first == 0) {
            return '|'
        } else {
            return '-'
        }
    }

    fun getPipe() = pipeAt(currentPos)

    fun toGraphChar(pipe: Char) : Char =
        when(pipe) {
            'L' -> '└'
            'J' -> '┘'
            '7' -> '┐'
            'F' -> '┌'
            '-' -> '─'
            '|' -> '│'
            'S' -> 'S'
            '.' -> 'X'
            ':' -> 'O'
            ';' -> 'M'
            else -> pipe
        }
}
